from turtle import Turtle

class Scoreboard(Turtle):
    def __init__(self):
        super().__init__()
        self.color("white")
        self.penup()
        self.hideturtle()
        self.l_score = 0
        self.r_score = 0

    def update_scoreboard(self):
        self.clear()
        self.goto(-100, 200)
        self.write(self.l_score, align="center", font=("Courier", 80, "normal"))
        self.goto(100, 200)
        self.write(self.r_score, align="center", font=("Courier", 80, "normal"))

    def l_point(self):
        self.l_score += 1
        self.update_scoreboard()

    def r_point(self):
        self.r_score += 1
        self.update_scoreboard()

    def end_game(self, winner):
        self.goto(0, 0)
        if winner == "l":
            self.write("Left side player has won the game.", align="center", font=("Courier", 20, "normal"))
        elif winner == "r":
            self.write("Right side player has won the game.", align="center", font=("Courier", 20, "normal"))
